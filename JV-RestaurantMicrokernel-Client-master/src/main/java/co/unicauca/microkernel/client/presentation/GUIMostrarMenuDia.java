    private void tblMenuDiaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMenuDiaMousePressed
        int indexFila = tblMenuDia.getSelectedRow();
        compId = components.get(indexFila).getCompId();
        Calendar c = Calendar.getInstance();
        //aÃ±o,mes,dia
        c.set(2021,10,12); // vairables int
        int day = c.get(Calendar.DAY_OF_WEEK);
        if (day == Calendar.SUNDAY) {
            //Domingo
            dia="Domingo";
        }
        if (day == Calendar.MONDAY) {
            //Lunes
            dia="Lunes";
        }
        if (day == Calendar.TUESDAY){
            dia="Martes";
        }
        if(day==Calendar.WEDNESDAY){
            dia="Miercoles";
        }
        if(day==Calendar.THURSDAY){
            dia="Jueves";
        }
        if(day==Calendar.FRIDAY){
            dia="Viernes";
        }
        
        
    }//GEN-LAST:event_tblMenuDiaMousePressed

